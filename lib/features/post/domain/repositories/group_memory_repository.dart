import '../../../../core/utils/typedefs.dart';

/// Контракт для сохранения и восстановления последней выбранной группы
/// при создании поста.
///
/// Используется для улучшения UX: при открытии экрана создания поста
/// автоматически выбирается группа, в которую пользователь публиковал
/// последний раз.
///
/// Хранение — локальное (SharedPreferences), очистка при логауте.
abstract interface class GroupMemoryRepository {
  /// Получить ID последней выбранной группы.
  ///
  /// Возвращает `null`, если группа ещё не была сохранена.
  ResultFuture<String?> getLastSelectedGroup();

  /// Сохранить ID выбранной группы.
  ///
  /// Вызывается после успешной публикации поста.
  ResultFuture<void> saveLastSelectedGroup(String groupId);

  /// Очистить сохранённую группу.
  ///
  /// Вызывается, если сохранённая группа больше не существует
  /// или пользователь не является её членом.
  ResultFuture<void> clearLastSelectedGroup();
}
